    @Override
    public void load() {
        this.jTextField1.setText(PhpcsOptions.getScript());
        this.jTextField2.setText(PhpcsOptions.getStandard());
        this.jTextField3.setText(PhpcsOptions.getExtensions());
        this.jTextField4.setText(PhpcsOptions.getIgnore());
        
        this.jCheckBox1.setSelected(PhpcsOptions.getWarnings());
    }

    @Override
    public void save() {
        PhpcsOptions.setScript(this.jTextField1.getText());
        PhpcsOptions.setStandard(this.jTextField2.getText());
        PhpcsOptions.setExtensions(this.jTextField3.getText());
        PhpcsOptions.setIgnore(this.jTextField4.getText());
        
        PhpcsOptions.setWarnings(this.jCheckBox1.isSelected());
    }

